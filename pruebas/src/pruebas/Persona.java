package pruebas;

public class Persona {
	private Integer doc;
	private String tipo_doc;
	private String name;
	private String surname;
	private Boolean sexo;
	
	public Persona(Integer doc, String tipo_doc, String name, String surname, Boolean sexo) {
		super();
		this.doc = doc;
		this.tipo_doc = tipo_doc;
		this.name = name;
		this.surname = surname;
		this.sexo = sexo;
	}

	
}
